        <h2>Política de Tratamiento de Datos Personales</h2>
        <p>
            Se adopta en cumplimiento de la <b>Ley 1581 de 2012</b>, el <b>Decreto 1377 de 2013</b> y demás normas que regulan la protección de datos personales en Colombia. Al utilizar este sitio web o suministrar información personal, el titular acepta el tratamiento de sus datos conforme a esta política.
        </p>
        <h4>1. Alcance del tratamiento</h4>
        <p>
            Los datos personales que el usuario suministre serán tratados de manera segura, confidencial y para las finalidades permitidas por la ley. No se compartirán con terceros sin autorización previa, salvo por obligaciones legales o requerimientos de autoridades.
        </p>
        <h4>2. Finalidades del tratamiento</h4>
        <ul>
            <li>Gestionar la relación con los usuarios del sitio web.</li>
            <li>Responder solicitudes, consultas o mensajes enviados.</li>
            <li>Enviar información sobre contenidos, actividades, servicios y actualizaciones del proyecto.</li>
            <li>Realizar análisis internos para mejorar la experiencia y funcionamiento del sitio.</li>
            <li>Cumplir deberes legales, contractuales y administrativos.</li>
        </ul>
        <h4>3. Derechos del titular</h4>
        <ul>
            <li>Conocer, actualizar y rectificar su información personal.</li>
            <li>Solicitar prueba de la autorización otorgada.</li>
            <li>Ser informado sobre el uso de sus datos.</li>
            <li>Presentar quejas ante la Superintendencia de Industria y Comercio (SIC).</li>
            <li>Solicitar la supresión de sus datos o revocar la autorización cuando sea procedente.</li>
        </ul>
        <h4>4. Deberes del responsable</h4>
        <ul>
            <li>Garantizar el uso adecuado de los datos personales.</li>
            <li>Implementar medidas de seguridad administrativas, técnicas y físicas; evitar pérdida, acceso no autorizado o alteración de la información.</li>
            <li>Usar los datos solo para las finalidades autorizadas.</li>
            <li>Atender solicitudes, reclamos y consultas del titular.</li>
        </ul>
        <h4>5. Conservación de la información</h4>
        <p>
            Los datos serán almacenados solo por el tiempo necesario para cumplir las finalidades requeridas o según la ley.
        </p>
        <h4>6. Aceptación</h4>
        <p>
            Al aceptar esta política y suministrar información personal, el usuario declara que ha leído y comprendido las condiciones aquí descritas y autoriza el tratamiento de sus datos conforme a la normativa vigente en Colombia.
        </p>
